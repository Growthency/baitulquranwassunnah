type Row = {
  slug: string;
  title: string;
  excerpt: string;
  cover: string;
  category: string;
  author: string;
  published_at: string;
  read_mins: number;
  blocks: BlogBlock[];
};

function rowToPost(r: Row): BlogPost {
  return {
    slug: r.slug,
    title: r.title,
    excerpt: r.excerpt,
    cover: r.cover,
    category: r.category,
    author: r.author,
    date: r.published_at,
    readMins: r.read_mins,
    blocks: r.blocks,
  };
}

export async function getAllPosts(): Promise<BlogPost[]> {
  const supabase = getSupabase();
  if (supabase) {
    const { data, error } = await supabase
      .from("blog_posts")
      .select("*")
      .eq("published", true)
      .order("published_at", { ascending: false });
    if (!error && data && data.length > 0) {
      return (data as Row[]).map(rowToPost);
    }
  }
  return [...seedPosts].sort((a, b) => (a.date < b.date ? 1 : -1));
}

export async function getPostBySlug(slug: string): Promise<BlogPost | null> {
  const supabase = getSupabase();
  if (supabase) {
    const { data, error } = await supabase
      .from("blog_posts")
      .select("*")
      .eq("slug", slug)
      .eq("published", true)
      .maybeSingle();
    if (!error && data) return rowToPost(data as Row);
  }
  return seedPosts.find((p) => p.slug === slug) ?? null;
}

export function getAllSlugs(): string[] {
  return seedPosts.map((p) => p.slug);
}
